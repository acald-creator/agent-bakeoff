// Compartment for language extensions — allows dynamic reconfiguration if needed
const langCompartment = new Compartment();

// ─── Component ────────────────────────────────────────────────────────────────

export function Editor(): JSX.Element {
  let container!: HTMLDivElement;
  let view!: EditorView;
  // Guard: prevents the update listener from treating programmatic note-load
  // dispatches as user edits that should trigger a save.
  let isLoadingNote = false;

  onMount(() => {
    // Dynamic import language-data so the entry chunk doesn't pay for all parsers.
    // The markdown extension still works without language-data (falls back to
    // no syntax highlighting in fenced blocks). Language-data loads ~30KB lazy.
    import('@codemirror/language-data').then(({ languages }) => {
      if (!view) return;
      view.dispatch({
        effects: langCompartment.reconfigure(
          markdown({ base: markdownLanguage, codeLanguages: languages }),
        ),
      });
    }).catch(() => {
      // Non-fatal — fenced code blocks work, just without per-language coloring
    });

    view = new EditorView({
      state: EditorState.create({
        doc: '',
        extensions: [
          basicSetup,
          // Initial lang config without language-data; upgraded lazily above
          langCompartment.of(markdown({ base: markdownLanguage })),
          inkEditorTheme,
          EditorView.updateListener.of((update) => {
            if (!update.docChanged || isLoadingNote) return;
            const id = store.activeNoteId;
            if (!id) return;
            setIsDirty(true);
            scheduleSave(id, update.state.doc.toString());
          }),
          EditorView.lineWrapping,
        ],
      }),
      parent: container,
    });
  });

  // ── Note switching ──────────────────────────────────────────────────────────
  createEffect(() => {
    const id = store.activeNoteId;
    if (!id || !view) return;

    isLoadingNote = true;
    clearTimeout(saveTimer);
    setIsDirty(false);

    const body = loadNoteBody(id);

    // Step 1: Dim the editor surface (CSS transition handles the fade)
    container.dataset.transitioning = 'true';

    // Step 2: Replace document and reset selection to start
    view.dispatch({
      changes: {
        from: 0,
        to:   view.state.doc.length,
        insert: body,
      },
      selection: EditorSelection.cursor(0),
    });

    // Step 3: Scroll to top
    view.dispatch({
      effects: EditorView.scrollIntoView(0, { y: 'start' }),
    });

    // Step 4: Un-dim after one animation frame
    requestAnimationFrame(() => {
      delete container.dataset.transitioning;
      isLoadingNote = false;
      view.focus();
    });
  });

  onCleanup(() => {
    clearTimeout(saveTimer);
    view?.destroy();
  });

  return (
    <div
      ref={container}
      class={css.editorRoot}
      aria-label="Note editor"
      role="textbox"
      aria-multiline="true"
    />
  );
}
