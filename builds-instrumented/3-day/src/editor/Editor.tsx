/**
 * Editor.tsx — CodeMirror 6 integration for Solid
 *
 * Design decisions:
 *   1. CodeMirror owns the document; Solid signals note switches only.
 *   2. 400ms debounced save: dirty dot appears immediately, localStorage deferred.
 *   3. Note switching uses CSS opacity crossfade (120ms) — no JS animation library.
 *   4. History cleared on note switch — Cmd+Z must NOT cross note boundaries.
 *   5. @codemirror/language-data is dynamic-imported (lazy chunk, not in entry).
 *   6. Compartment used for dynamic reconfiguration (not EditorState.reconfigure).
 *
 * Known landmines avoided:
 *   - @codemirror/language is a transitive dep; we only import from codemirror
 *     and @codemirror/lang-markdown (both in package.json).
 *   - EditorState.reconfigure does NOT exist; Compartment is used instead.
 */
import {
  onMount,
  onCleanup,
  createEffect,
  createSignal,
  type JSX,
} from 'solid-js';
import { EditorView, basicSetup }       from 'codemirror';
import { EditorState, EditorSelection, Compartment } from '@codemirror/state';
import { markdown, markdownLanguage }   from '@codemirror/lang-markdown';
import { store, applyNoteSave }         from '../app/store';
import { loadNoteBody }                 from '../lib/persistence';
import * as css                         from './editor.css';

// ─── Dirty-state signal (exported for sidebar dirty dot indicator) ─────────────
export const [isDirty, setIsDirty] = createSignal(false);

// ─── Debounce ─────────────────────────────────────────────────────────────────
let saveTimer: ReturnType<typeof setTimeout>;

function scheduleSave(id: string, body: string): void {
  clearTimeout(saveTimer);
  saveTimer = setTimeout(() => {
    const firstLine = body.split('\n').find((l) => l.trim()) ?? '';
    const title = firstLine.replace(/^#+\s*/, '').trim() || 'Untitled';
    applyNoteSave(id, title, body);
    setIsDirty(false);
  }, 400);
}

// ─── CodeMirror theme ─────────────────────────────────────────────────────────
