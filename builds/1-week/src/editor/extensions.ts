/**
 * extensions.ts — CodeMirror 6 extension bundle for Ink.
 *
 * This module assembles the CM6 extension list that defines the editor's
 * behavior and appearance. It is separate from Editor.tsx so that the
 * extension list can be tested and modified without touching the Solid
 * component lifecycle.
 *
 * Extension choices explained:
 *
 *   - basicSetup: CM6's opinionated starter pack. Includes history (Cmd+Z),
 *     closeBrackets, syntax highlighting, autocompletion base, and more.
 *
 *   - markdown(): Full GFM markdown language support. Includes:
 *     - Inline decorations (bold renders bold, italic renders italic)
 *     - Header detection
 *     - Link and code detection
 *     - Fenced code blocks with nested language parsing (via `codeLanguages`)
 *
 *   - languages (from @codemirror/language-data): Lazy-loaded language
 *     definitions for fenced blocks. The initial bundle is tiny; language
 *     parsers are loaded on demand. This is the code-split chunk.
 *
 *   - EditorView.lineWrapping: Essential for a writing tool. Without this,
 *     long markdown lines extend past the viewport.
 *
 *   - inkEditorTheme: CM6 theme built from the tokens.ts CSS custom properties.
 *     Using CSS var() references means the theme responds to :root changes
 *     (future dark mode) without rebuilding the EditorView.
 */
