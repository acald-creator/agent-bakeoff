// ─── Extension factory ────────────────────────────────────────────────────────

/**
 * Build the full extension array for a new EditorView.
 * The update listener is passed in so Editor.tsx owns the callback lifecycle.
 */
export function buildExtensions(onUpdate: (update: ViewUpdate) => void): Extension[] {
  return [
    // minimalSetup provides: history, drawSelection, highlightActiveLine,
    // closeBrackets, bracketMatching, search (Ctrl+F), default keymaps.
    // It does NOT include lineNumbers() or foldGutter() — right for a writing tool.
    minimalSetup,
    markdown({ base: markdownLanguage, codeLanguages: languages }),
    inkEditorTheme,
    syntaxHighlighting(inkHighlightStyle),
    EditorView.lineWrapping,
    EditorView.updateListener.of(onUpdate),
  ];
}
